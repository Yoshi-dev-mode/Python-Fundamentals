"""
TERNARY OPERATOR - is a one-line shortcut for writing an if-else statement.
FORMAT: value_if_true if condition else value_if_false

"""

# Example No. 1
age = 1
message = print("Adult") if age >= 90 else print("hs")

# Example No. 2
a = 10
b = 20
bigger = a if a > b else b
