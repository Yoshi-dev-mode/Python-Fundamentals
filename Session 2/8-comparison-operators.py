"""
COMPARISON OPERATORS:
    1. == (Equal to) - Returns True if both values are equal
    2. != (Not equal to) - Returns True if values are not equal
    3. > (Greater than) - Returns True if the left value is greater than the right
    4. < (Less than) - Returns True if the left value is smaller than the right
    5. >= (Greater than or equal to) - Returns True if the left value is greater than or equal to the right
    6. <= (Less than or equal to) - Returns True if the left value is less than or equal to the right
    
"""

# Comparison Operators Example

a = 10
b = 10

# Equal to
print("a == b :", a == b)   # Is a equal to b?

# Not equal to
print("a != b :", a != b)   # Is a not equal to b? 

# Greater than
print("a > b  :", a > b)    # Is a greater than b?

# Less than
print("a < b  :", a < b)    # Is a less than b?

# Greater than or equal to
print("a >= b :", a >= b)   # Is a greater than or equal to b?

# Less than or equal to
print("a <= b :", a <= b)   # Is a less than or equal to b?

